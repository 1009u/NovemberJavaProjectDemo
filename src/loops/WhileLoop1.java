package loops;

public class WhileLoop1 {
    public static void main(String[] args) {
        //Syntax: while (condition) {code}
        int i=1;
        while (i<=10){
            System.out.println(i*2);
            i++;
        }

        // hello print 10 time
        int a=1;
        while (a<=10){
            System.out.println("Hello.."+a);
            a++;
        }
    }
}

package variables;

public class DemoBoolean1 {
    public static void main(String[] args) {
        // Boolean return true and false for any value
        // Simple boolean

        boolean winterCold=true;
        boolean iceHot=false;
        System.out.println(winterCold);
        System.out.println(iceHot);

        //we will use boolean with integer
        int x=5;
        int y=10;
        System.out.println(x<y);

        // true or false
        int a=45;
        System.out.println(a==45);

        // true false
        int age = 22;
        int licence_age=21;
        System.out.println(age>licence_age);

    }
}
